#!/usr/bin/env node

const fs = require("node:fs")
const path = require("node:path")

const args = process.argv.slice(2)
const targetArg = args[0]

const sourceRoot = path.resolve(__dirname, "..")
const targetDir = path.resolve(process.cwd(), targetArg || ".")

const excluded = new Set([
  ".git",
  ".github",
  ".next",
  "node_modules",
  ".env.local",
  "package-lock.json",
  "npm-debug.log",
  "README.md",
  "LICENSE",
  "scripts",
])

const includeFromRoot = [
  "src",
  "public",
  "check-env-variables.js",
  "next-env.d.ts",
  "next-sitemap.js",
  "next.config.js",
  "postcss.config.js",
  "tailwind.config.js",
  "tsconfig.json",
  ".eslintrc.json",
  ".prettierrc.json",
  "STOREFRONT_SETUP.md",
  "package.json",
]

function copyRecursive(fromPath, toPath) {
  const stat = fs.statSync(fromPath)

  if (stat.isDirectory()) {
    fs.mkdirSync(toPath, { recursive: true })

    for (const entry of fs.readdirSync(fromPath)) {
      if (excluded.has(entry)) {
        continue
      }

      copyRecursive(path.join(fromPath, entry), path.join(toPath, entry))
    }

    return
  }

  fs.copyFileSync(fromPath, toPath)
}

function writeEnvExample() {
  const envExample = `NEXT_PUBLIC_MEDUSA_PUBLISHABLE_KEY=pk_xxxxxxxxxxxxxxxxxxxxxxxxxx
NEXT_PUBLIC_MEDUSA_BACKEND_URL=http://localhost:9000
NEXT_PUBLIC_DEFAULT_REGION=dk
NEXT_PUBLIC_BASE_URL=http://localhost:8001
NEXT_PUBLIC_STRIPE_KEY=
NEXT_PUBLIC_MEDUSA_PAYMENTS_PUBLISHABLE_KEY=
NEXT_PUBLIC_MEDUSA_PAYMENTS_ACCOUNT_ID=
NEXT_PUBLIC_RAZORPAY_KEY_ID=
NEXT_PUBLIC_VERCEL_URL=http://localhost:8001
MEDUSA_CLOUD_S3_HOSTNAME=
MEDUSA_CLOUD_S3_PATHNAME=
`

  fs.writeFileSync(path.join(targetDir, ".env.example"), envExample)
}

function updatePackageJson() {
  const packageJsonPath = path.join(targetDir, "package.json")
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"))

  packageJson.name = targetArg || "brutalist-medusa-storefront"
  packageJson.private = true

  delete packageJson.bin
  delete packageJson.files
  delete packageJson.publishConfig

  fs.writeFileSync(packageJsonPath, `${JSON.stringify(packageJson, null, 2)}\n`)
}

function writeStarterReadme() {
  const content = `# Brutalist Medusa Storefront

Generated by create-brutalist-medusa-storefront.

## Quick start

1. Install dependencies:
   npm install
2. Copy env file:
   cp .env.example .env.local
3. Start dev server:
   npm run dev

App URL: http://localhost:8001
`

  fs.writeFileSync(path.join(targetDir, "README.md"), content)
}

function run() {
  if (!targetArg) {
    console.error("Please provide a target folder. Example: npx create-brutalist-medusa-storefront my-storefront")
    process.exit(1)
  }

  if (fs.existsSync(targetDir) && fs.readdirSync(targetDir).length > 0) {
    console.error(`Target directory is not empty: ${targetDir}`)
    process.exit(1)
  }

  fs.mkdirSync(targetDir, { recursive: true })

  for (const relPath of includeFromRoot) {
    const fromPath = path.join(sourceRoot, relPath)
    const toPath = path.join(targetDir, relPath)
    copyRecursive(fromPath, toPath)
  }

  writeEnvExample()
  updatePackageJson()
  writeStarterReadme()

  console.log(`Created Brutalist Medusa storefront at ${targetDir}`)
  console.log("Next steps:")
  console.log(`  cd ${targetArg}`)
  console.log("  npm install")
  console.log("  cp .env.example .env.local")
  console.log("  npm run dev")
}

run()
